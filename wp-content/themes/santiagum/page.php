<p>Geboren im Jahr 1982, wuchs ich im ländlich-beschaulichen Oberbayern auf und wohne seit 2005 in dessen Landeshauptstadt <strong>München</strong>.</p>

<h2 id="wasichmag">Was ich mag:</h2>

<p>Seit meiner Jugend faszinieren mich zwei Dinge: Die <strong>Musik</strong> und das <strong>Schreiben</strong>. Na gut, drei Dinge: Musik, Schreiben und Fußball. Und Lesen. OK, vier Dinge. Aber mehr kommen jetzt nicht mehr dazu.</p>

<p>Da sich eine Fußball-Karriere aus naheliegenden (körperlichen und talentbedingten) Gründen schnell zerschlug, verfolgte ich meine anderen Leidenschaften umso stärker. Meine Laufbahn als Bassist und Sänger einer <a href="http://www.lastfm.de/music/Fake+the+band?ac=fake%20the%20band">ziemlich großartigen Punkband</a> zeichnete sich vor allem durch jahrelanges Ausweichen jeder öffentlichen Aufmerksamkeit aus. Nach insgesamt drei aufgenommenen Alben verschlug es mich auf die "andere Seite". Seit 2009 verfasse ich Musikrezensionen für das Online-Portal <a href="http://www.plattentests.de/suche.php?parameter=autor&amp;suche=Mark+Read">www.plattentests.de</a>, und seit Ende 2012 hat auch das Schriftstellerische dank der Self-Publishing-Plattform <a href="http://www.bookrix.de/-pedestrian/books.html">www.bookrix.de</a> endlich ein Ventil gefunden.</p>

<p>Die Musik hat mich aber natürlich nicht losgelassen. Auf <a href="https://soundcloud.com/unarmedpedestrian">Soundcloud</a> veröffentliche ich von Zeit zu Zeit selbst aufgenommene Sachen unter dem Namen Unarmed Pedestrian.</p>
