                <p>Coming straight to the point without using too many words or beating around the bush is a tough task for every writer. While some authors I like describe everything that might be important for the story in detail and thus use up tons of pages without advancing the story, others, like Ernest Hemingway, give away almost nothing and leave it up to the reader to determine what might be important. The only thing that second group is interested in is the plot itself. <br />
So it is everything but easy to write a whole story in just 81 words. This is where <a href="http://81words.net/">81words.net</a> comes into play. </p>

<p>I stumbled upon that website by total coincidence. When I signed up to <a href="https://twitter.com/MarkReadM">Twitter</a> a few months ago, one of the first accounts to follow mine was 81words.net. Don’t know how they found me, but once I checked out their website, I was ready to go. <br />
The goal on 81words.net is to write a story in that exact amount of words. Not 80, not 82, but exactly 81. There are no restrictions as to genre, topic or whatever. Every participant can write what he or she likes – and then let the other uses rate their story. The concept immediately appealed to me, and so I jumped right into it.</p>

<p>I don’t normally write in English since my primary language in everyday life is German (even though I was brought up in a sort-of-bilingual way). But there doesn’t seem to be a website like 81words.net in German. Furthermore, I quickly realized that it actually is a nice challenge to write in another language – even if it’s a language that I grew up with. </p>

<p>Up until now, I have written three short stories on 81words.net (<a href="http://81words.net/author/markread/">you can visit my profile page here</a>) – with more to come in the next weeks. It seems that you have to sign up to be able to read and rate them. So if you don’t want to become an author yourself, you probably won’t do it. Since I’m a nice guy, I’m showing you two of my stories here. Enjoy!</p>

<h2 id="hemsmojito">Hem's mojito</h2>

<p>“This is a dream, right?“, I asked cautiously.
“Smart little guy”, Hemingway said and ordered a mojito at the bar.
“So, what am I doing here together with you?”
Hemingway leaned back and looked me in the eye. “You want to be a writer, don’t you? I’m a nobel prize winner. Ask me anything you want. That’s what dreams are for.” <br />
I pondered that. Then I asked Hemingway: “How’s the mojito?” <br />
He frowned. “I’d take the daiquiri if I were you.”</p>

<h2 id="itneveris">It never is</h2>

<p>He laughed. Then he started to cry. He shook his head. Then he sighed. <br />
“How long?”
“I give you forty-eight hours”, the doctor said, “maybe seventy-two.”
He thanked the doctor and went outside, where he took a cab. <br />
“I love you”, he said as she opened her apartment door.
She laughed. Then she started to cry. She shook her head. Then she sighed. <br />
“Don’t you think it’s a little too late for that?”, she asked.
“It never is”, he said smiling.</p>
